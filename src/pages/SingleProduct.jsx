import axios from "axios";
import{ useEffect, useState } from "react";
import { useParams } from "react-router-dom";
import Loading from "../assets/Loading4.webm";
import Breadcrums from "../components/Breadcrums";
import { IoCartOutline } from "react-icons/io5";
import { useCart } from "../context/CartContext";

function SingleProduct() {
  const params = useParams();
  const [singleproduct, setSingleProduct] = useState("");
  const {addToCart} = useCart();
  const getSingleProducts = async () => {
    try {
      const res = await axios.get(
        `https://fakestoreapi.com/products/${params.id}`
      );
      const product = res.data;
      setSingleProduct(product);
      // console.log(product);
    } catch (error) {
      console.log(error);
    }
  };
  useEffect(() => {
    getSingleProducts();
  }, []);

  const originalPrice = Math.round(singleproduct.price + (singleproduct.price * 0.5))
  return (
    <>
      {singleproduct ? (
        <div className="px-4 pb-4 md:px-0 ">
          <Breadcrums title={singleproduct.title} />
          <div className="max-w-6xl mx-auto md:p-6 grid grid-cols-1 md:grid md:grid-cols-2 gap-10">
            <div className="w-full">
              <img src={singleproduct.image} alt={singleproduct.title} className="rounded-2xl w-full object-cover"/>
            </div>
            <div className="flex flex-col gap-6">
              <h1 className="md:text-3xl text-3xl font-bold text-gray-800 sm:text-xl">{singleproduct.title}</h1>
              <div className="text-gray-700 ">{singleproduct.category?.toUpperCase()}</div>
              <p className="text-xl text-red-500 font-bold">${singleproduct.price} <span className="line-through text-gray-700">${originalPrice}</span> <span className="bg-red-500 text-white px-4 py-1 rounded-full ">20% discount</span></p>
              <p className="text-gray-600">{singleproduct.description}</p>

              <div className="flex items-center gap-4">
                <label htmlFor="" className="text-sm font-medium text-gray-700">Quantity:</label>
                <input type="number" min={1} value={1} className="w-20 border border-gray-300 rounded-lg px-3 py-1 focus:outline-none focus:ring-2 focus:ring-red-500"/>
              </div>
              <div className="flex gap-4 mt-4">
                <button onClick={()=>addToCart(singleproduct )} className="px-6 flex gap-2 py-2 text-lg bg-red-500 text-white rounded-lg"><IoCartOutline className="h-6 w-6"/>Add to Cart</button>
              </div>
            </div>
          </div>
        </div>
      ) : (
        <div className="flex items-center justify-center h-screen">
          <video muted autoPlay loop>
            <source src={Loading} type="video/webm" />
          </video>
        </div>
      )}
    </>
  );
}

export default SingleProduct;
